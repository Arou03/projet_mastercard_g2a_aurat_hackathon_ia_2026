create or replace dynamic table M2_ISD_EQUIPE_5_DB.MACHINE_LEARNING.WEATHER_FREQ_JOINED(
	WEEK,
	SAISON,
	SATURDAY_TS,
	FREQ,
	AVG_PRECIPITATIONS_MM,
	AVG_PRECIPITATIONS_DUREE_MN,
	AVG_TEMP_MIN,
	AVG_TEMP_MAX,
	AVG_TEMP_MOYENNE,
	AVG_TEMP_AMPLITUDE,
	AVG_VENT_MOYEN,
	AVG_VENT_RAFALE_MAX,
	DIST_HOLIDAY_BEL,
	DIST_HOLIDAY_DEU,
	DIST_HOLIDAY_FRA,
	DIST_HOLIDAY_GBR,
	DIST_HOLIDAY_NLD,
	DIST_HOLIDAY_POL,
	DIST_HOLIDAY_SCAN,
	DIST_HOLIDAY_USA
) target_lag = '1 day' refresh_mode = AUTO initialize = ON_CREATE warehouse = HACKATHON_WH
 as
WITH weather_weekly AS (
    SELECT
        'S' || WEEKOFYEAR(DATE)::VARCHAR AS WEEK,
        CASE
            WHEN WEEKOFYEAR(DATE) >= 40 THEN 'H' || ((YEAR(DATE) % 100) + 1)::VARCHAR
            ELSE 'H' || (YEAR(DATE) % 100)::VARCHAR
        END AS SAISON,
        AVG(PRECIPITATIONS_MM_MOY) AS AVG_PRECIPITATIONS_MM,
        AVG(PRECIPITATIONS_DUREE_MN_MOY) AS AVG_PRECIPITATIONS_DUREE_MN,
        AVG(TEMP_MIN_MOY) AS AVG_TEMP_MIN,
        AVG(TEMP_MAX_MOY) AS AVG_TEMP_MAX,
        AVG(TEMP_MOYENNE) AS AVG_TEMP_MOYENNE,
        AVG(TEMP_AMPLITUDE_MOY) AS AVG_TEMP_AMPLITUDE,
        AVG(VENT_MOYEN_MOY) AS AVG_VENT_MOYEN,
        AVG(VENT_RAFALE_MAX_MOY) AS AVG_VENT_RAFALE_MAX
    FROM M2_ISD_EQUIPE_5_DB.CLEANED.WEATHER_FRANCE
    WHERE DEPARTEMENT_CODE IN ('73', '74')
    GROUP BY WEEK, SAISON
),
base AS (
    SELECT
        f.WEEK,
        f.SAISON,
        t.SATURDAY_TS,
        f.FREQ,
        w.AVG_PRECIPITATIONS_MM,
        w.AVG_PRECIPITATIONS_DUREE_MN,
        w.AVG_TEMP_MIN,
        w.AVG_TEMP_MAX,
        w.AVG_TEMP_MOYENNE,
        w.AVG_TEMP_AMPLITUDE,
        w.AVG_VENT_MOYEN,
        w.AVG_VENT_RAFALE_MAX
    FROM M2_ISD_EQUIPE_5_DB.CLEANED.FREQ_GLOBAL f
    INNER JOIN weather_weekly w
        ON f.WEEK = w.WEEK
        AND f.SAISON = w.SAISON
    LEFT JOIN M2_ISD_EQUIPE_5_DB.DIMENSION.DIM_TIME t
        ON f.SAISON = t.SAISON
        AND f.WEEK = t.WEEK
    QUALIFY ROW_NUMBER() OVER (PARTITION BY f.WEEK, f.SAISON ORDER BY t.SATURDAY_TS) = 1
),
holiday_dist_raw AS (
    SELECT
        b.WEEK,
        b.SAISON,
        h.CODE_PAYS,
        CASE
            WHEN b.SATURDAY_TS::DATE BETWEEN h.DEBUT::DATE AND h.FIN::DATE THEN 0
            WHEN b.SATURDAY_TS::DATE < h.DEBUT::DATE THEN DATEDIFF('day', b.SATURDAY_TS::DATE, h.DEBUT::DATE)
            ELSE -DATEDIFF('day', h.FIN::DATE, b.SATURDAY_TS::DATE)
        END AS SIGNED_DIST
    FROM base b
    CROSS JOIN M2_ISD_EQUIPE_5_DB.CLEANED.CALENDAR_SCHOOL_HOLIDAYS h
),
-- Étape 1 : On isole la vacance la plus proche par pays et par semaine
holiday_closest AS (
    SELECT 
        WEEK, 
        SAISON, 
        CODE_PAYS, 
        SIGNED_DIST
    FROM holiday_dist_raw
    QUALIFY ROW_NUMBER() OVER (PARTITION BY WEEK, SAISON, CODE_PAYS ORDER BY ABS(SIGNED_DIST), SIGNED_DIST DESC) = 1
),
-- Étape 2 : On pivote les résultats pour avoir une ligne par (WEEK, SAISON)
holiday_pivoted AS (
    SELECT
        WEEK,
        SAISON,
        MAX(CASE WHEN CODE_PAYS = 'BEL' THEN SIGNED_DIST END) AS DIST_HOLIDAY_BEL,
        MAX(CASE WHEN CODE_PAYS = 'DEU' THEN SIGNED_DIST END) AS DIST_HOLIDAY_DEU,
        MAX(CASE WHEN CODE_PAYS = 'FRA' THEN SIGNED_DIST END) AS DIST_HOLIDAY_FRA,
        MAX(CASE WHEN CODE_PAYS = 'GBR' THEN SIGNED_DIST END) AS DIST_HOLIDAY_GBR,
        MAX(CASE WHEN CODE_PAYS = 'NLD' THEN SIGNED_DIST END) AS DIST_HOLIDAY_NLD,
        MAX(CASE WHEN CODE_PAYS = 'POL' THEN SIGNED_DIST END) AS DIST_HOLIDAY_POL,
        MAX(CASE WHEN CODE_PAYS = 'SCAN' THEN SIGNED_DIST END) AS DIST_HOLIDAY_SCAN,
        MAX(CASE WHEN CODE_PAYS = 'USA' THEN SIGNED_DIST END) AS DIST_HOLIDAY_USA
    FROM holiday_closest
    GROUP BY WEEK, SAISON
)
SELECT
    b.*,
    hp.DIST_HOLIDAY_BEL,
    hp.DIST_HOLIDAY_DEU,
    hp.DIST_HOLIDAY_FRA,
    hp.DIST_HOLIDAY_GBR,
    hp.DIST_HOLIDAY_NLD,
    hp.DIST_HOLIDAY_POL,
    hp.DIST_HOLIDAY_SCAN,
    hp.DIST_HOLIDAY_USA
FROM base b
LEFT JOIN holiday_pivoted hp
    ON b.WEEK = hp.WEEK 
    AND b.SAISON = hp.SAISON;