create or replace dynamic table M2_ISD_EQUIPE_5_DB.MACHINE_LEARNING.ML_EXPENSES_FEATURES(
	PAYS,
	SATURDAY,
	SAISON,
	DEPENSES,
	WEEK_OF_YEAR,
	MONTH,
	JOURS_ANTICIPATION,
	PAYS_AVG_DEPENSES,
	PAYS_STD_DEPENSES,
	HAS_HOLIDAY,
	LAG_1,
	LAG_2,
	ROLLING_MEAN_3,
	PCT_CHANGE
) target_lag = '1 day' refresh_mode = AUTO initialize = ON_CREATE warehouse = HACKATHON_WH
 as
WITH country_stats AS (
    SELECT 
        PAYS,
        AVG(DEPENSES) AS PAYS_AVG_DEPENSES,
        STDDEV(DEPENSES) AS PAYS_STD_DEPENSES
    FROM M2_ISD_EQUIPE_5_DB.CLEANED.FACT_EXPENSES_INTERNATIONAL
    GROUP BY PAYS
),
holidays_flag AS (
    SELECT 
        h.CODE_PAYS,
        d.SATURDAY_TS,
        1 AS HAS_HOLIDAY
    FROM M2_ISD_EQUIPE_5_DB.CLEANED.CALENDAR_SCHOOL_HOLIDAYS h
    JOIN M2_ISD_EQUIPE_5_DB.DIMENSION.DIM_TIME d
        ON d.DATE_TS BETWEEN h.DEBUT AND h.FIN
    GROUP BY h.CODE_PAYS, d.SATURDAY_TS
),
base AS (
    SELECT 
        e.PAYS,
        e.DEPENSES,
        e.DATE AS SATURDAY,
        e.SAISON,
        EXTRACT(WEEK FROM e.DATE) AS WEEK_OF_YEAR,
        EXTRACT(MONTH FROM e.DATE) AS MONTH,
        EXTRACT(YEAR FROM e.DATE) AS YEAR,
        COALESCE(a.JOURS_ANTICIPATION, 120) AS JOURS_ANTICIPATION,
        COALESCE(cs.PAYS_AVG_DEPENSES, 0) AS PAYS_AVG_DEPENSES,
        COALESCE(cs.PAYS_STD_DEPENSES, 0) AS PAYS_STD_DEPENSES,
        COALESCE(hf.HAS_HOLIDAY, 0) AS HAS_HOLIDAY,
        LAG(e.DEPENSES, 1) OVER (PARTITION BY e.PAYS ORDER BY e.DATE) AS LAG_1,
        LAG(e.DEPENSES, 2) OVER (PARTITION BY e.PAYS ORDER BY e.DATE) AS LAG_2,
        AVG(e.DEPENSES) OVER (PARTITION BY e.PAYS ORDER BY e.DATE ROWS BETWEEN 3 PRECEDING AND 1 PRECEDING) AS ROLLING_MEAN_3
    FROM M2_ISD_EQUIPE_5_DB.CLEANED.FACT_EXPENSES_INTERNATIONAL e
    LEFT JOIN M2_ISD_EQUIPE_5_DB.REF.REF_ANTICIPATION a ON e.PAYS = a.CODE_PAYS
    LEFT JOIN country_stats cs ON e.PAYS = cs.PAYS
    LEFT JOIN holidays_flag hf ON e.PAYS = hf.CODE_PAYS AND e.DATE = hf.SATURDAY_TS
)
SELECT 
    PAYS,
    SATURDAY,
    SAISON,
    DEPENSES,
    WEEK_OF_YEAR,
    MONTH,
    JOURS_ANTICIPATION,
    PAYS_AVG_DEPENSES,
    PAYS_STD_DEPENSES,
    HAS_HOLIDAY,
    COALESCE(LAG_1, PAYS_AVG_DEPENSES) AS LAG_1,
    COALESCE(LAG_2, PAYS_AVG_DEPENSES) AS LAG_2,
    COALESCE(ROLLING_MEAN_3, PAYS_AVG_DEPENSES) AS ROLLING_MEAN_3,
    CASE WHEN LAG_1 IS NOT NULL AND LAG_1 != 0 
         THEN (LAG_1 - COALESCE(LAG_2, LAG_1)) / LAG_1 
         ELSE 0 END AS PCT_CHANGE
FROM base;