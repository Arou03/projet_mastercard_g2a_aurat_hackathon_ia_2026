create or replace dynamic table M2_ISD_EQUIPE_5_DB.CLEANED.FACT_EXPENSES_INTERNATIONAL(
	PAYS,
	DEPENSES,
	DATE,
	SAISON
) target_lag = '1 hour' refresh_mode = AUTO initialize = ON_CREATE warehouse = HACKATHON_WH
 as
SELECT
    CODE_PAYS AS PAYS,
    DEPENSES,
    TO_TIMESTAMP(SATURDAY::VARCHAR, 'YYYY-MM-DD') AS DATE,
    SAISON
FROM M2_ISD_EQUIPE_5_DB.RAW.DEPENSES_PROVENANCE;