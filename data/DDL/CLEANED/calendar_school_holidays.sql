create or replace dynamic table M2_ISD_EQUIPE_5_DB.CLEANED.CALENDAR_SCHOOL_HOLIDAYS(
	CODE_PAYS,
	TYPE_VACANCES,
	SAISON,
	DEBUT,
	FIN
) target_lag = '1 hour' refresh_mode = AUTO initialize = ON_CREATE warehouse = HACKATHON_WH
 as
SELECT
    UPPER(TRIM(CODE_PAYS)) AS CODE_PAYS,
    UPPER(TRIM(TYPE_VACANCES)) AS TYPE_VACANCES,
    UPPER(TRIM(SAISON)) AS SAISON,

    -- agrégation + conversion en timestamp
    TO_TIMESTAMP(MIN(DEBUT)) AS DEBUT,
    TO_TIMESTAMP(MAX(FIN)) AS FIN

FROM M2_ISD_EQUIPE_5_DB.RAW.CALENDRIER_VACANCES_SCOLAIRES

WHERE DEBUT IS NOT NULL
  AND FIN IS NOT NULL

GROUP BY
    CODE_PAYS,
    TYPE_VACANCES,
    SAISON;