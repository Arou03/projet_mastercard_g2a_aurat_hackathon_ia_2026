CREATE OR REPLACE DYNAMIC TABLE CLEANED.freq_global_per_dept
    TARGET_LAG = '4 hour'
    WAREHOUSE = 'HACKATHON_WH'
    COMMENT = 'Table croisant la fréquentation H24 et la distribution par département'
AS
SELECT 
    r.CODE_DEPARTEMENT,
    'H24' AS SAISON,
    f.WEEK,
    TRY_CAST(REPLACE(f.H24, ' ', '') AS FLOAT) * d.RURAL AS RURAL,
    TRY_CAST(REPLACE(f.H24, ' ', '') AS FLOAT) * d.URBAIN AS URBAIN,
    TRY_CAST(REPLACE(f.H24, ' ', '') AS FLOAT) * d.TOTAL_AURA AS TOTAL_AURA,
    TRY_CAST(REPLACE(f.H24, ' ', '') AS FLOAT) * d.STATIONS_MONTAGNE AS STATIONS_MONTAGNE, 
    TRY_CAST(REPLACE(f.H24, ' ', '') AS FLOAT) * d.VILLAGES_MONTAGNE AS VILLAGES_MONTAGNE  
FROM 
    RAW.FREQ_GLOBALE f
CROSS JOIN 
    RAW.DISTRIBUTION_NUITEES_AURA d
LEFT JOIN 
    REF.REF_DEPARTEMENT r 
    ON d.DEPARTEMENT = r.NOM_DEPARTEMENT;