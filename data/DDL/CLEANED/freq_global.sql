create or replace dynamic table M2_ISD_EQUIPE_5_DB.CLEANED.FREQ_GLOBAL(
	WEEK,
	SAISON,
	FREQ
) target_lag = '1 day' refresh_mode = AUTO initialize = ON_CREATE warehouse = HACKATHON_WH
 as
SELECT WEEK, 'H19' AS SAISON, H19 AS FREQ FROM M2_ISD_EQUIPE_5_DB.RAW.FREQ_GLOBALE
UNION ALL
SELECT WEEK, 'H20' AS SAISON, H20 AS FREQ FROM M2_ISD_EQUIPE_5_DB.RAW.FREQ_GLOBALE
UNION ALL
SELECT WEEK, 'H21' AS SAISON, H21 AS FREQ FROM M2_ISD_EQUIPE_5_DB.RAW.FREQ_GLOBALE
UNION ALL
SELECT WEEK, 'H22' AS SAISON, H22 AS FREQ FROM M2_ISD_EQUIPE_5_DB.RAW.FREQ_GLOBALE
UNION ALL
SELECT WEEK, 'H23' AS SAISON, H23 AS FREQ FROM M2_ISD_EQUIPE_5_DB.RAW.FREQ_GLOBALE
UNION ALL
SELECT WEEK, 'H24' AS SAISON, H24 AS FREQ FROM M2_ISD_EQUIPE_5_DB.RAW.FREQ_GLOBALE
UNION ALL
SELECT WEEK, 'H25' AS SAISON, H25 AS FREQ FROM M2_ISD_EQUIPE_5_DB.RAW.FREQ_GLOBALE
ORDER BY SAISON, WEEK;